package com.teraninja.mohamy.Model;

public class Registermodel {
    public String mobile;
    public String name;
    public String email;
    public int client_code;
    public int mobile_code;
    public String client_mobile;
    public int id;

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getClient_code() {
        return client_code;
    }

    public void setClient_code(int client_code) {
        this.client_code = client_code;
    }

    public int getMobile_code() {
        return mobile_code;
    }

    public void setMobile_code(int mobile_code) {
        this.mobile_code = mobile_code;
    }

    public String getClient_mobile() {
        return client_mobile;
    }

    public void setClient_mobile(String client_mobile) {
        this.client_mobile = client_mobile;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
