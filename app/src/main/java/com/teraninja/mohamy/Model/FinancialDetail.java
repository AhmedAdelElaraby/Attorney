package com.teraninja.mohamy.Model;

public class FinancialDetail {
    public int creditor;
    public int debtor;
    public int net_wege;

    public int getCreditor() {
        return creditor;
    }

    public void setCreditor(int creditor) {
        this.creditor = creditor;
    }

    public int getDebtor() {
        return debtor;
    }

    public void setDebtor(int debtor) {
        this.debtor = debtor;
    }

    public int getNet_wege() {
        return net_wege;
    }

    public void setNet_wege(int net_wege) {
        this.net_wege = net_wege;
    }
}
