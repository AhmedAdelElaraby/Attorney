package com.teraninja.mohamy.Model;

public class ELMalyaD {
    public int id;
    public String name;
    public String email;
    public String mobile;
    public int trials_count;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public int getTrials_count() {
        return trials_count;
    }

    public void setTrials_count(int trials_count) {
        this.trials_count = trials_count;
    }
}
